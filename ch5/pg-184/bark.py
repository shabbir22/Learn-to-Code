def bark(name, weight):   #Defining a function in the name of 'bark' #variables we are going to supply in are name and weight
    if weight > 20:
        print(name, 'says WOOF WOOF')
    else:
        print(name, 'says woof woof')

#Now, call the function 'bark' and supply its variables names (name, weight)
bark('Codie', 40)
bark('Sparky', 9)
bark('Jackson', 12)
bark('Fido', 65)


